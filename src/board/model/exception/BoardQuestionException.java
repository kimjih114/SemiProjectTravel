package board.model.exception;

public class BoardQuestionException extends RuntimeException {

	public BoardQuestionException() {
		super();
	}

	public BoardQuestionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BoardQuestionException(String message, Throwable cause) {
		super(message, cause);
	}

	public BoardQuestionException(String message) {
		super(message);
	}

	public BoardQuestionException(Throwable cause) {
		super(cause);
	}

	
}

